moved = false
function onCheck()
    if Ground_2:getRotation() > -10 and Ground_2:getRotation() < 15 then
        if time == 0 then
            time = world:getTime()
        end
    else
        time = 0
    end

    if moved == false and time ~= 0 and world:getTime() - time > 5 then
        Ground_4:setPosition(-1000, -1000)
        moved = true
    end

    return trigger_3:getState()
end
